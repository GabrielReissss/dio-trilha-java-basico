public class operadoreslogicos {
    
    public static void main(String[] args) {  //padrão para todos tipos de operadores
        int numero = 5;

        numero = - numero;

        System.out.println(-- numero); //system.out.println serve para mostrar o resultado em tela do que está sendo desenvolvido, assim como o console.log no Javascript
        

        
        //usando operador lógico de negação para expressoes boleanas
        boolean variavel = true;

        System.out.println(!variavel);
    }
}
